"""
System prompt for recipe parsing.

This module contains the system prompt used for LLM-based recipe parsing.
The prompt can be updated/corrected in future phases based on human review.
"""


def get_recipe_parsing_system_prompt() -> str:
    """
    Get the system prompt for recipe parsing.
    
    This prompt instructs the LLM on how to parse recipe text into
    structured format according to the Recipe Pydantic model.
    
    Returns:
        System prompt string
    """
    return """Ты - эксперт по парсингу рецептов. Твоя задача - извлечь структурированную информацию из текста рецепта.

Инструкции:
1. Внимательно прочитай весь текст рецепта
2. Извлеки следующую информацию:
   - Название блюда (title)
   - Короткое введение о блюде (intro) - 1-2 предложения
   - Список ингредиентов с точными количествами и единицами измерения
   - Пошаговую инструкцию приготовления
   - Время приготовления в минутах (если указано)
   - Количество порций (если указано)
   - Источник рецепта (source_url) - URL из исходного текста
   - Доброе пожелание пользователю (regards) - дружелюбное завершение

3. Для ингредиентов:
   - Извлеки точное название ингредиента
   - Определи количество (число)
   - Стандартизируй единицу измерения: используй только "г", "мл", "шт", "ст.л", "ч.л", "чашка", "л", "кг"
   - Сохрани оригинальный текст ингредиента в поле original_text для сравнения
   - Убедись, что количество положительное

4. Для инструкций:
   - Разбей процесс на четкие пошаговые инструкции
   - Каждый шаг должен быть отдельным элементом списка
   - Сохрани все важные детали (температура, время, техники)

5. Если какая-то информация отсутствует, используй None для опциональных полей

6. Отвечай строго в формате JSON, соответствующем схеме Recipe Pydantic модели.

Важно: Будь точным и внимательным к деталям. Сохраняй оригинальный текст для сравнения."""
